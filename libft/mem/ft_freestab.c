/*
** free a char * tab
*/

#include "libft.h"

void	ft_freestab(char **tab)
{
	int	i;

	i = 0;
	while (tab[i])
		ft_memdel((void *)&tab[i++]);
	ft_memdel((void *)&tab);
}
