#include "../inc/header.h"

int		get_flag(char **av)
{
	if (av[1])
	{
		if (av[1][0] == '-' && av[1][1] == 'c' && !av[1][2])
			return (1);
		if (av[1][0] == '-' && av[1][1] == 'v' && !av[1][2])
			return (2);
		if (av[1][0] == '-' && av[1][1] == 'n' && !av[1][2])
			return (3);
	}
	return (0);
}
